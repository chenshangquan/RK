//  发送REQ指令之前检查内部状态
#define NORMAL_CHECK                                    \
{                                                       \
    if (m_wState != MCSLIB_STATE_NORMAL)                \
    {                                                   \
        ::OspLog( LOGLVL_EXCEPTION,                     \
                 _T("[MCSLIB]m_wState(%d) is not MCSLIB_STATE_NORMAL!\n"),  \
                 m_wState );                            \
        return MCSLIB_NOT_IN_NORMAL_STATE;			    \
    }                                                   \
    if (m_emTimerState != emTimerWaitNormal)	        \
    {                                                   \
        ::OspLog( LOGLVL_EXCEPTION,                     \
                  _T("[MCSLIB]m_emTimerState(%d:%s) is not TIMER_WAIT_NORMAL!\n"), \
                  m_emTimerState, GetTimerOspHint( m_emTimerState ) );                     \
        return MCSLIB_MUST_WAIT;					    \
    }                                                   \
}


#define STATE_DEAL(pcSession)                       \
{                                                   \
    if ( ! pcSession->IsSerialNoTrue(&cServMsg) )   \
    {                                               \
        return;                                     \
    }                                               \
    pHandler->McslibKillTimer( TIMER_WAIT_REPLY );     \
    pcSession->m_emTimerState = emTimerWaitNormal;  \
}

